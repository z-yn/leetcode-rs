impl Solution {
    pub fn two_sum(nums: Vec<i32>, target: i32) -> Vec<i32> {
        let mut map = HashMap::new();
        for (i,&v) in nums.iter().enumerate() {
            if let Some(&i1) = map.get(&(target-v)) {
                return vec![i1,i as i32];
            } else {
                map.insert(v,i as i32);
            }
        }
        vec![]
    }
}
